#pragma once

namespace prism {
    namespace scene {

        /// @brief Компоненты трансформации для позиционирования, вращения и масштабирования объектов в 3D-пространстве
        /// @details Предоставляет базовые структуры для описания преобразований объектов в сцене

        /// @brief Компонент позиции в 3D-пространстве
        /// @details Хранит координаты объекта по осям X, Y, Z
        /// @note По умолчанию инициализируется нулевыми значениями (0, 0, 0)
        struct Position {
            float x = 0;    /// Координата по оси X
            float y = 0;    /// Координата по оси Y  
            float z = 0;    /// Координата по оси Z
        };

        /// @brief Компонент вращения в 3D-пространстве
        /// @details Хранит углы вращения вокруг осей X, Y, Z в градусах или радианах
        /// (зависит от реализации системы трансформаций)
        /// @note По умолчанию инициализируется нулевыми значениями (0, 0, 0)
        /// @warning Порядок применения вращений может влиять на финальную ориентацию
        struct Rotation {
            float x = 0;    /// Угол вращения вокруг оси X (в градусах/радианах)
            float y = 0;    /// Угол вращения вокруг оси Y (в градусах/радианах)
            float z = 0;    /// Угол вращения вокруг оси Z (в градусах/радианах)
        };

        /// @brief Компонент масштабирования в 3D-пространстве
        /// @details Хранит коэффициенты масштабирования по осям X, Y, Z
        /// @note По умолчанию инициализируется единичными значениями (1, 1,  1)
        /// @warning Отрицательные значения масштаба могут вызывать инверсию объекта
        struct Scale {
            float x = 1;    /// Коэффициент масштабирования по оси X
            float y = 1;    /// Коэффициент масштабирования по оси Y
            float z = 1;    /// Коэффициент масштабирования по оси Z
        };

        /// @brief Компонент трансформации объекта в 3D-пространстве
        /// @details Объединяет позицию, вращение и масштаб для полного описания
        /// преобразования объекта в мировом пространстве.
        /// 
        /// Используется системами рендеринга, физики и анимации для определения
        /// положения и ориентации объектов в сцене.
        /// 
        /// @example
        /// ```cpp
        /// TransformComponent transform;
        /// transform.pos = {10.0f, 5.0f, 0.0f};  // Позиция
        /// transform.rot = {0.0f, 45.0f, 0.0f};  // Поворот на 45° вокруг Y
        /// transform.scale = {2.0f, 2.0f, 2.0f}; // Увеличение в 2 раза
        /// ```
        struct TransformComponent
        {
            Position pos;   /// Позиция объекта в мировом пространстве
            Rotation rot;   /// Вращение объекта вокруг осей
            Scale scale;    /// Масштаб объекта по осям
        };
    }
}